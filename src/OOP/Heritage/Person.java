package OOP.Heritage;

public class Person {
    protected String name;
    protected int age;
    protected String address;
    protected String city;
    protected String country;

    public Person(final String name, final int age, final String address, final String city, final String country) {
        this.name = name;
        this.age = age;
        this.address = address;
        this.city = city;
        this.country = country;
    }




}
